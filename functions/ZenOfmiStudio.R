The Zen of mistudio

Less is better than more, but sometimes more is required to make less
Each function should not depend on the result of its peer functions
Among understandability, simplicity, and speed, understandability > simplicity > speed
Between redundance and complexity, redundance > complexity.
Always choose base package functions than non-base packages.
One function only returns one variable.
The generation of a final variable should only consist of one line of code.
Never use <<-
99% of the run time is spent on analysis functions, so optimizing mistudio code gives little gain
"/functions" functions should return the result of the calculation only
Comments is better than no comments, more comments is better than comments--write code as though you are writting an essay
For functions in "/functions", every input is assume to be valid. The checking work is left to server
